import turtle
t=turtle.Pen()

import time

def topcenter(distance):
	t.up()
	t.left(90)
	t.forward(distance)
	t.left(90)
	t.down()

def lgcircle():
	t.begin_fill()
	t.color('yellow')
	t.circle(200)
	t.end_fill()
	t.color('black')
	t.pensize(2)
	t.circle(200)

def face():
	t.up()
	t.left(90)
	t.forward(150)
	t.right(90)
	t.forward(50)
	t.right(90)
	t.down()
	t.begin_fill()
	t.circle(30)
	t.end_fill()
	t.up()
	t.right(90)
	t.forward(100)
	t.right(90)
	t.down()
	t.begin_fill()
	t.circle(30)
	t.end_fill()
	t.up()
	t.forward(75)
	t.right(90)
	t.down()
	t.begin_fill()
	t.forward(100)
	t.left(90)
	t.circle(50,180)
	t.end_fill()

def wink():
    t.speed(0)
    t.up()
    t.forward(75)
    t.down()
    t.begin_fill()
    t.color('yellow')
    t.circle(-35)
    t.end_fill()
    t.pensize(12)
    t.right(90)
    t.color('black')
    t.forward(60)
    t.backward(60)
    t.left(35)
    t.forward(60)
    t.backward(60)
    t.right(70)
    t.forward(60)
    t.backward(60)
    time.sleep(1.0)
    t.right(55)
    t.begin_fill()
    t.color('yellow')
    t.circle(45)
    t.end_fill()
    t.begin_fill()
    t.color('black')
    t.pensize(2)
    t.circle(30)
    t.end_fill()

def smile1w():
    topcenter(200)
    lgcircle()
    face()
    wink()

t.hideturtle()
smile1w()
